    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(et_razon.getText().length()>25 || et_domicilio.getText().length()>25 || et_localidad.getText().length()>25){
            
            JOptionPane.showMessageDialog(null, "Los campos deben ser menores a 25 caracteres", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
        
        }else if(et_razon.getText().contains(";")||et_domicilio.getText().contains(";")||et_localidad.getText().contains(";")){
            
            JOptionPane.showMessageDialog(null, "Los campos no deben contener \";\"", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
        
        }else if(et_razon.getText().trim().length()<1 || et_domicilio.getText().trim().length()<1 || et_localidad.getText().trim().length()<1){
        
                    JOptionPane.showMessageDialog(null, "Completar todos los campos", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
        
        }else{
            File file = new File("empresas.csv");
            if(!file.exists()){
                try {
                    file.createNewFile();
                } catch (IOException ex) {
                    Logger.getLogger(AltaEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                try {
                    FileWriter fw = new FileWriter(file,true);
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.write("\n");
                    bw.write(et_razon.getText()+";"+et_localidad.getText()+";"+et_domicilio.getText());
                    bw.close();
                    et_razon.setText("");
                    et_domicilio.setText("");
                    et_localidad.setText("");
                    JOptionPane.showMessageDialog(null, "Registro Guardado exitosamente", "Guardar", JOptionPane.INFORMATION_MESSAGE);
                    
                } catch (IOException ex) {
                    Logger.getLogger(AltaEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

