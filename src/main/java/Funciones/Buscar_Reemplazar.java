    public int buscar() {
        JTable tabla = principal.getTabla();
        int columna = tabla.getSelectedColumn();
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        String valorBus = buscarS.getText();
        String valorRee = reemplazarS.getText();
        boolean hallado = false;
        reemplazados = 0;
        if(columna==-1){
            JOptionPane.showMessageDialog(null,"Seleccione celda de columna a buscar",
                    "No ha seleccionado ninguna columna",JOptionPane.WARNING_MESSAGE);
            this.dispose();
        }

        for (int i = 0; i < modelo.getRowCount(); i++) {

            String celda = (String) modelo.getValueAt(i, columna);
            System.out.println(celda);
            if (null == celda) {
                celda = "";
            }

            if (celda.contains(valorBus)) {

                String replace = celda.replace(valorBus, valorRee);
                modelo.setValueAt(replace, i, columna);
                hallado = true;
                reemplazados++;

            }
        }

        return reemplazados;
    }

